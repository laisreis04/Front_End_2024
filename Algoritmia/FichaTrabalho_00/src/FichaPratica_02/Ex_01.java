package FichaPratica_02;

public class Ex_01 {

    public static void main(String[] args){

        //Iniciar com uma variavel
        int numero = 1;

        //Ciclo de repetição

        while(numero >= 1 && numero <= 250){

            System.out.println(numero);

            //Incrementar
            numero++;
        }

    }


}
